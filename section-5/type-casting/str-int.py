
# string -> integer 

score_english = "90"
score_mathematics = "95" 
score_programming = "100" 

# convert strings to integers 

# int :: built-in function 

score_english = int(score_english) 
score_mathematics = int(score_mathematics) 
score_programming = int(score_programming) 

# calculate the average score 

average_score = ( score_mathematics + score_english + score_programming ) / 3 

# print the average score 

print(average_score) 

