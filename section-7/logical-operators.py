## Logical Operators 

#  and operator

# 

is_weekend = True 
is_sunny = True 

if is_weekend and is_sunny:
    print("Lets go for picnic") 
else:
    print("Oops we can not go to the picnic") 


# or operator 

is_novel = True 
price = 15

if is_novel or price < 10:
    print("I will buy the Book") 
else:
    print("I will not buy the Book") 

# not operator 

has_pasta = True 

if not has_pasta:
    print("Time to order pizza.") 
else:
    print("No need to order Pizza") 

